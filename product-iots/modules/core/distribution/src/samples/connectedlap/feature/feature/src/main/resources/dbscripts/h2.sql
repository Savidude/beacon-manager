CREATE  TABLE IF NOT EXISTS `CONNECTEDLAP_DEVICE` (
  `CONNECTEDLAP_DEVICE_ID` VARCHAR(45) NOT NULL ,
  `DEVICE_NAME` VARCHAR(100) NULL DEFAULT NULL,
  `DEVICE_DISC` FLOAT NULL DEFAULT NULL,
  `DEVICE_MEMORY` FLOAT NULL DEFAULT NULL,
  `DEVICE_CPU_INFO` VARCHAR(100) NULL DEFAULT NULL,
  `DEVICE_NETWORK` VARCHAR(100) NULL DEFAULT NULL,
  `DEVICE_CPU_CORE` INT NULL DEFAULT NULL,
  PRIMARY KEY (`CONNECTEDLAP_DEVICE_ID`) );



